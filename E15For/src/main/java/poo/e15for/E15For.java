//Estructura repetitiva fija (for)

package poo.e15for;

public class E15For {

    public static void main(String[] args) {
        
        //Visualizar numeros pares 2-20
        for (int i = 2; i <= 20; i=i+2) {
            System.out.println(i);
        }
        
        //Visualizar numeros impares 1-20
        for (int i = 1; i < 20; i=i+2) {
            System.out.println(i);
        }
    }
}
